package List;

public class SoccerGame {
    private String localTeam;
    private String visitTeam;
    private Integer localScore;
    private Integer visiteScore;

    public String getLocalTeam() {
        return localTeam;
    }

    public void setLocalTeam(String localTeam) {
        this.localTeam = localTeam;
    }

    public String getVisitTeam() {
        return visitTeam;
    }

    public void setVisitTeam(String visitTeam) {
        this.visitTeam = visitTeam;
    }

    public Integer getLocalScore() {
        return localScore;
    }

    public void setLocalScore(Integer localScore) {
        this.localScore = localScore;
    }

    public Integer getVisiteScore() {
        return visiteScore;
    }

    public void setVisiteScore(Integer visiteScore) {
        this.visiteScore = visiteScore;
    }
}
